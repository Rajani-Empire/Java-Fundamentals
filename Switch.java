import java.util.*;


public class Switch {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        int x=sc.nextInt();

        switch(x){
            case 1:
                System.out.println("hello");
                break;
            case 2:
                System.out.println("my");
                break;
            
            case 3:
                System.out.println("friend");
                break;
            default :
                System.out.println("rajani");
                break;
        }

    }
    
}
